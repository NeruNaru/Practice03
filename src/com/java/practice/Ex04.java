package com.java.practice;

public class Ex04 {

	public static void main(String[] args) {
		for(int i = 1 ; i < 5 ; i++) {
			System.out.println(i);
		}
		//i가 4가 될 때 까지 i의 값을 출력
		
		System.out.println("=====================");
		
		for(int i=10 ; i>0 ; i=i-2) {
			System.out.println(i);
		}
		//i가 10일 때 i를 계속해서 2를 빼 0이 되기 전 까지 i 값을 출력
	}

}
